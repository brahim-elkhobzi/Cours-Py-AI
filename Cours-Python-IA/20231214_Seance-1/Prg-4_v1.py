# Initialisation de la liste
liste_initiale = [1, 3, 5, 7, 9]
print("Liste initiale :", liste_initiale)

# Renversement de la liste avec l'opérateur [::-1]
liste_renversee_1 = liste_initiale[::-1]
print("Liste renversée :", liste_renversee_1)

# Renversement d'une sous-liste spécifique
liste_renversee_2 = liste_initiale[3:1:-1]
print("Liste renversée (sous-liste) :", liste_renversee_2)